package com.iu.computer;

public class Vga {
	
	private int size;
	private String company;
	
	public Vga(int size, String company) {
		this.size=size;
		this.company=company;
	}
	
	public int getSize() {
		return size;
	}
	public void setSize(int size) {
		this.size = size;
	}
	public String getCompany() {
		return company;
	}
	public void setCompany(String company) {
		this.company = company;
	}
	
	

}
